package faconnicolas.project.tetris.model.window;

/**
 * Positionable interface is here to know the position on a window.
 */
public interface Positionable {

    /**
     * get X Position
     *
     * @return X Position
     */
    int getX();

    /**
     * get Y Position
     *
     * @return Y Position
     */
    int getY();
}
